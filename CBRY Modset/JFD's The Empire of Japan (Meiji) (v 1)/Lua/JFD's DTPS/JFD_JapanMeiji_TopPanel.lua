function JFD_GetJapanMeijiCultureBonus(playerID)
	local player = Players[playerID]
	local cultureBonus = math.floor(player:GetTotalJONSCulturePerTurn() * 20/100)
	if (not player:IsGoldenAge()) then
		cultureBonus = 0
	end

	return cultureBonus
end
